package org.jreleaser.maven.plugin;

import static org.jreleaser.util.StringUtils.isNotBlank;

/**
 * @author Andres Almiray
 * @since 0.1.0
 */
abstract class AbstractRepositoryTap implements Activatable {
    protected Active active;
    private String owner;
    private String name;
    private String branch;
    private String username;
    private String token;
    private String commitMessage;

    void setAll(AbstractRepositoryTap tap) {
        this.active = tap.active;
        this.owner = tap.owner;
        this.name = tap.name;
        this.branch = tap.branch;
        this.username = tap.username;
        this.token = tap.token;
        this.commitMessage = tap.commitMessage;
    }

    @Override
    public Active getActive() {
        return active;
    }

    @Override
    public void setActive(Active active) {
        this.active = active;
    }

    @Override
    public String resolveActive() {
        return active != null ? active.name() : null;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getCommitMessage() {
        return commitMessage;
    }

    public void setCommitMessage(String commitMessage) {
        this.commitMessage = commitMessage;
    }

    public boolean isSet() {
        return active != null ||
            isNotBlank(owner) ||
            isNotBlank(name) ||
            isNotBlank(branch) ||
            isNotBlank(username) ||
            isNotBlank(token) ||
            isNotBlank(commitMessage);
    }
}
